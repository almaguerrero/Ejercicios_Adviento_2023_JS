# hacer un programa que solicite dos números 
# y muestre a suma, resta, multitplcacion y divisición
num_1 = input('Dame el primer número:  ')
num_2 = input('Dame el segundo número:  ')
print("la suma es: ", int(num_1)+int(num_2))
print("la resta es: ", int(num_2)-int(num_1))
print("la mult es: ", int(num_1)*int(num_2))
print("la div es: ", int(num_2)/int(num_1))